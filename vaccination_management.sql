CREATE TABLE Clients (
    ClientID CHAR(12) PRIMARY KEY,
    Name VARCHAR(100),
    DOB DATE,
    Contact VARCHAR(20)
);

CREATE TABLE Vaccines (
    VaccineID CHAR(6) PRIMARY KEY,
    Name VARCHAR(100),
    Manufacturer VARCHAR(100),
    DosesRequired INT
);

CREATE TABLE Admins (
    AdminID CHAR(12) PRIMARY KEY,
    User VARCHAR(100),
    PasswordHash VARCHAR(255)
);

CREATE TABLE Vaccination_Records (
    RecordID INT PRIMARY KEY,
    ClientID CHAR(12),
    VaccineID CHAR(6),
    DateGiven DATE,
    NextDueDate DATE,
    AdministeredBy VARCHAR(100),
    FOREIGN KEY (ClientID) REFERENCES Clients(ClientID),
    FOREIGN KEY (VaccineID) REFERENCES Vaccines(VaccineID)
);

CREATE TABLE Deliverable (
    DeliverableID INT PRIMARY KEY,
    ReportName VARCHAR(100),
    CreatedOn DATE,
    CreatedBy CHAR(12),
    FOREIGN KEY (CreatedBy) REFERENCES Admins(AdminID)
);


